#!/bin/bash

#Перевіряємо, чи передав користувач хоча б один аргумент
if [ -z "$1" ]; then
    echo "Помилка: Ви не вказали назву файлу."
    echo "Використання: $0 <назва_файлу>"
    exit 1
fi

#Зберігаємо перший аргумент у змінну
filename=$1

#Перевіряємо, чи існує такий файл
if [ ! -f "$filename" ]; then
    echo "Помилка: Файл '$filename' не знайдено."
    exit 1
fi

#Рахуємо кількість рядків за допомогою wc (word count)
#Прапор -l означає "lines"
line_count=$(wc -l < "$filename")

#Виводимо результат
echo "У файлі '$filename' знайдено рядків: $line_count"
